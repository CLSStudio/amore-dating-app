  Widget _buildEnhancedDimensionBar(
    String leftLabel, 
    String rightLabel, 
    int leftScore, 
    int rightScore,
    Color leftColor,
    Color rightColor,
  ) {
    final total = leftScore + rightScore;
    final leftPercentage = total > 0 ? leftScore / total : 0.5;
    
    return Column(
      children: [
        Row(
          mainAxisAlignment: MainAxisAlignment.spaceBetween,
          children: [
            Text(
              leftLabel,
              style: TextStyle(
                fontSize: 15,
                fontWeight: FontWeight.w600,
                color: leftColor,
              ),
            ),
            Text(
              rightLabel,
              style: TextStyle(
                fontSize: 15,
                fontWeight: FontWeight.w600,
                color: rightColor,
              ),
            ),
          ],
        ),
        const SizedBox(height: 12),
        Container(
          height: 12,
          decoration: BoxDecoration(
            borderRadius: BorderRadius.circular(6),
            color: Colors.grey[200],
          ),
          child: Row(
            children: [
              Expanded(
                flex: (leftPercentage * 100).round(),
                child: Container(
                  decoration: BoxDecoration(
                    borderRadius: const BorderRadius.only(
                      topLeft: Radius.circular(6),
                      bottomLeft: Radius.circular(6),
                    ),
                    gradient: LinearGradient(
                      colors: [leftColor.withOpacity(0.8), leftColor],
                    ),
                  ),
                ),
              ),
              Expanded(
                flex: ((1 - leftPercentage) * 100).round(),
                child: Container(
                  decoration: BoxDecoration(
                    borderRadius: const BorderRadius.only(
                      topRight: Radius.circular(6),
                      bottomRight: Radius.circular(6),
                    ),
                    gradient: LinearGradient(
                      colors: [rightColor, rightColor.withOpacity(0.8)],
                    ),
                  ),
                ),
              ),
            ],
          ),
        ),
        const SizedBox(height: 8),
        Row(
          mainAxisAlignment: MainAxisAlignment.spaceBetween,
          children: [
            Container(
              padding: const EdgeInsets.symmetric(horizontal: 8, vertical: 4),
              decoration: BoxDecoration(
                color: leftColor.withOpacity(0.1),
                borderRadius: BorderRadius.circular(8),
              ),
              child: Text(
                '${(leftPercentage * 100).round()}%',
                style: TextStyle(
                  fontSize: 12,
                  color: leftColor,
                  fontWeight: FontWeight.w600,
                ),
              ),
            ),
            Container(
              padding: const EdgeInsets.symmetric(horizontal: 8, vertical: 4),
